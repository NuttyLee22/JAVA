package command;

public class Switch {
	
	public void on() {
		System.out.println("Switch Ligado");
	}
	
	public void off() {
		System.out.println("Switch Desligado");
	}
	
	public void login() {
		System.out.println("Usuario logado");
	}
	
	public void logoff() {
		System.out.println("Usuario deslogado");
	}
}
